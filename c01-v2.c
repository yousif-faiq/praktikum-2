static uint32_t lfsr_tick(uint32_t s) {
    uint32_t bit = ((s >> 31) ^ (s >> 21) ^ (s >> 1) ^ s) & 1;
    return (s >> 1) | (bit << 31);
}

static uint32_t vrot(uint32_t v, uint32_t ctrl) {
    int r = (ctrl & 0x1F) | 1;
    return (v << r) | (v >> (32 - r));
}

static uint32_t fold64(uint32_t a, uint32_t b) {
    uint64_t w = (uint64_t)a * (uint64_t)(b | 1);
    return (uint32_t)((w >> 32) ^ (w & 0xFFFFFFFF));
}

static uint32_t sbox_word(uint32_t x) {
    return ((uint32_t)SB[(x >> 24) & 0xFF] << 24) |
           ((uint32_t)SB[(x >> 16) & 0xFF] << 16) |
           ((uint32_t)SB[(x >>  8) & 0xFF] <<  8) |
           ((uint32_t)SB[ x        & 0xFF]);
}

typedef uint32_t (*fn_t)(uint32_t);

static uint32_t f00(uint32_t x) { return (x * 0x6B43)    ^ 0xA5A5; }
static uint32_t f01(uint32_t x) { return vrot(x, x >> 3) ^ 0x1234; }
static uint32_t f02(uint32_t x) { return ~x ^ (x >> 7); }
static uint32_t f03(uint32_t x) { return (x + 0xDEAD)    * 0x45; }
static uint32_t f04(uint32_t x) { return fold64(x, 0xBEEF); }
static uint32_t f05(uint32_t x) { return (x * x)          ^ (x >> 4); }
static uint32_t f06(uint32_t x) { return ~(x + 0xFF)      ^ 0x5A5A; }
static uint32_t f07(uint32_t x) { return vrot(x, x ^ 0xCAFE) & 0xFFFF; }
static uint32_t f08(uint32_t x) { return fold64(x, x ^ 0x1337); }
static uint32_t f09(uint32_t x) { return (x ^ 0xABCD)     + vrot(x, 7); }
static uint32_t f10(uint32_t x) { return sbox_word(x)      ^ (x * 0x1B); }
static uint32_t f11(uint32_t x) { return fold64(~x, x + 0x9999); }
static uint32_t f12(uint32_t x) { return vrot(x * 0x1D, x >> 5); }
static uint32_t f13(uint32_t x) { return (x + 0x5555)      ^ sbox_word(x >> 8); }
static uint32_t f14(uint32_t x) { return fold64(x ^ 0xFF00, vrot(x, 13)); }
static uint32_t f15(uint32_t x) { return ~vrot(x, x ^ (x >> 16)); }

static fn_t table[16] = {
    f00, f01, f02, f03, f04, f05, f06, f07,
    f08, f09, f10, f11, f12, f13, f14, f15
};

static int compute_idx(uint32_t hi, uint32_t lo, uint32_t lfsr) {
    int outer = (hi ^ lfsr)        & 0x3;
    int inner = (lo ^ (lfsr >> 4)) & 0x3;
    return (outer << 2) | inner;
}

static uint32_t dispatch(uint32_t seed, int steps) {
    uint32_t hi   = seed >> 16;
    uint32_t lo   = seed & 0xFFFF;
    uint32_t lf   = lfsr_tick(seed);

    for (int i = 0; i < steps; i++) {
        int idx = compute_idx(hi, lo, lf);

        uint32_t combined = (hi << 16) | (lo & 0xFFFF);
        uint32_t result   = table[idx](combined);

        result = sbox_word(result);

        uint32_t new_hi = (result >> 16) ^ lo ^ (uint32_t)i;
        uint32_t new_lo = (result & 0xFFFF) ^ hi ^ SB[i & 0xFF];

        hi = new_hi & 0xFFFF;
        lo = new_lo & 0xFFFF;

        lf = lfsr_tick(lf ^ hi ^ lo);
    }

    return ((hi ^ lo) ^ SB[(hi ^ lo) & 0xFF]) & 0xFF;
}

